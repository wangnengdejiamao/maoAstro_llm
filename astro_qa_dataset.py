def create_dataset():
    """创建训练数据集"""
    output_dir = Path("output")
    output_dir.mkdir(exist_ok=True)
    
    all_qa = []
    for topic_data in ASTRO_KNOWLEDGE:
        for qa in topic_data["qa_pairs"]:
            qa["topic"] = topic_data["topic"]
            all_qa.append(qa)
    
    # 为每个主题生成更多变体问题
    extended_qa = []
    for qa in all_qa:
        extended_qa.append(qa)
        # 添加问题的不同表述方式
        if "什么是" in qa["question"]:
            extended_qa.append({
                "question": qa["question"].replace("什么是", "请解释"),
                "answer": qa["answer"],
                "topic": qa["topic"]
            })
            extended_qa.append({
                "question": qa["question"].replace("什么是", "简述"),
                "answer": qa["answer"][:200] + "..." if len(qa["answer"]) > 200 else qa["answer"],
                "topic": qa["topic"]
            })
    
    # 打乱数据
    random.seed(42)
    random.shuffle(extended_qa)
    
    # 划分训练集和测试集 (80/20)
    split_idx = int(len(extended_qa) * 0.8)
    train_data = extended_qa[:split_idx]
    test_data = extended_qa[split_idx:]
    
    # 保存
    with open(output_dir / "astro_train.json", 'w', encoding='utf-8') as f:
        json.dump(train_data, f, ensure_ascii=False, indent=2)
    
    with open(output_dir / "astro_test.json", 'w', encoding='utf-8') as f:
        json.dump(test_data, f, ensure_ascii=False, indent=2)
    
    with open(output_dir / "astro_full_dataset.json", 'w', encoding='utf-8') as f:
        json.dump(extended_qa, f, ensure_ascii=False, indent=2)
    
    print(f"数据集创建完成！")
    print(f"- 总QA对: {len(extended_qa)}")
    print(f"- 训练集: {len(train_data)}")
    print(f"- 测试集: {len(test_data)}")
    print(f"- 主题数: {len(ASTRO_KNOWLEDGE)}")
    
    return train_data, test_data

if __name__ == "__main__":
    create_dataset()
